void	ft_swap(int *a, int *b)
{
	int	aux;

	aux = *a;
	*a = *b;
	*b = aux;
}

int	main(void)
{
	int	a;
	int	b;
	int	*c;
	int	*d;

	a = 4;
	b = 2;
	c = &a;
	d = &b;
	ft_swap(c, d);
}
