int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int	main(void)
{
	char	*name;
	int		result;

	name = "Alicia";
	result = ft_strlen("Alicia");
	return (0);
}
